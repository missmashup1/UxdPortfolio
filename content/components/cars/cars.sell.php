    <h2>Responsive Design of Pages</h2>
    <p>
        In making Sell & Trade responsive, we were looking to eliminate as much unessential user input as possible
        (especially on mobile) and cut down on the length of the forms. We began the process with wireframing --
        both on paper and in Axure and validating decisions with hallway&nbsp;testing.
    </p>
    <p>
        As part of the effort to make it more
        friendly, we introduced a system of explanatory fly-outs to explain car lingo throughout the forms.
        Following the latest best practices of form design, like in-line validation, clear error flagging, form labels
        shown in the fields, etc. helped to enhance the UX experience. In the visual design, we were aligning to the
        company’s ever-evolving and expanding digital style&nbsp;framework.
    </p>

