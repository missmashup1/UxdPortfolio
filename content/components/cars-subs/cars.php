    <h2>User Observation</h2>
    <p>
        Empathizing with our end users was a starting point for this initiative. We sent several UX team
        members into the homes of different user types (based on loosely-sketched Personas) to
        observe and interview them using the VDP in natural, field settings. After a few rounds of this,
        enough information was collected for patterns of pain points to emerge. These got absorbed into
        planning the redesign. We would continue to do user testing throughout the project, but moving
        forward, users were recruited to come in to our user lab to interact with prototypes.
    </p>

    <h2>User Stories and Story Mapping</h2>
    <p>
        Our team started out working in 2-week sprints, or iteration cycles. For each, we would prioritize
        the work to be done by writing user stories. Instead of specifications for new features described in
        technical terms, user stories describe features from the point of view of the customer and follow the
        “role-feature-reason”&nbsp;format:
    </p>
    <p class="quote">
        “As a User, I want to submit my personal information<br>so that I can
        establish contact with a car&nbsp;seller.”
    </p>
    <p class="quote">
        “As a User, I want to easily inspect car<br>photos so that I can make quality car&nbsp;selections.”
    </p>
    <p>
        Such stories helped tie user needs to feature development and created a shared understanding among
        team members on roadmap details.  As we learned more about what customers wanted, we could move
        things around in the product backlog, the queue of stories yet to be built.  We created digital and
        physical story maps to view the experience holistically and help prioritize for MVP and for subsequent
        releases. Story mapping helped us see the big picture of the product.
    </p>

