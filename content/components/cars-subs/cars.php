    <h2>A/B Testing</h2>
    <p>
        In order to know that the changes made were related to the results we were seeing and that we
        were drawing the right lessons from those changes, we would use split-tests. Split-test
        experiments are ones in which different versions of a product are offered to users at the same
        time. By observing the changes in behavior between two groups, we could make inferences
        about the impact of the different variations.

    </p>
    <p>
        To assure a scientific result, both product versions
        are identical in all respects but the one being tested. In Agile this is called “A/B Testing” after the
        practice of assigning letter names to each variation. To figure out if a new design was effective,
        we would look at key performance metrics like the number of lead submissions, number of
        clicks, time spent on the page, bounce rate, numbers of new visitors, and numbers of return
        visitors.
    </p>
    <p>
        A/B testing often uncovers surprising things. Many features that make the product better in the
        eyes of designers and engineers have no impact on user behavior. Although working with A/B
        tests requires extra accounting and metrics to keep track of each variation, it almost always
        saves time in the long run by eliminating work that doesn’t matter to users.
    </p>

