    <h2>m.Cars.com</h2>
    <p>
        At the time of this project, users on mobile devices were directed to a separate, m-dot version of
        the site. The content of the wired VDP had to be stripped down to focus on the most essential
        content and be easy to scan on-the-go. Its branding had to reflect that of the wired site, but be
        touch-friendly. Influenced by the explosion of smartphone use, we made the move to go
        responsive in 2015 and we transitioned to re-thinking all content to be “mobile-first.”
    </p>

