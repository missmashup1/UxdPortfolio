    <h2>Results for the Business</h2>
    <p>
        In the new VDP delivered a cleaner and more engaging design with a larger photo experience,
        better vehicle differentiation, and easier to consume sections of content, like Seller’s Notes and
        Car Features. The lead conversion rate improved by 62%, resulting in $36M yearly run-rate of
        increased value to customers. We synced up with another team during the redesign to support
        vehicle saving/favoriting from the VDP for Consumer Profiles. The new design worked in a new
        ad unit for national advertisers and new logo sizes for advertisers subscribed to a tier of their
        affiliation with us. Dealers and national advertisers got a faster platform to merchandise their
        vehicles and feature ads.
    </p>


